let numero1 = prompt("Digite o primeiro número: ");
let numero2 = prompt("Digite o segundo número: ");

let n1 = parseFloat(numero1);
let n2 = parseFloat(numero2);

let soma = n1 + n2;
let subtracao = n1 - n2;
let multiplicacao = n1 * n2;
let divisao = n1 / n2;




alert(`A soma entre os dois números é igual a ${soma}
\n A subtração entre os dois números é igual a ${subtracao}
\n A multiplicação entre os dois números é igual a ${multiplicacao}
\n A divisão entre os dois númros é igual a ${divisao}`)
